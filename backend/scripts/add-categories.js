async function main() {
  const client = new pg.Client({ connectionString: process.env.DATABASE_URL });
  await client.connect();
  try {
    await client.query(`
      CREATE TABLE IF NOT EXISTS categories (
        id SERIAL PRIMARY KEY,
        name VARCHAR(100) NOT NULL UNIQUE,
        description TEXT,
        sort_order INTEGER NOT NULL DEFAULT 0,
        created_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
      )
    `);

    const cats = [
      ['Кімнатні рослини', 'Монстера, фікус, сансевієрія та інші зелені улюбленці', 1],
      ['Квіти', 'Орхідеї, фіалки, антуріуми — квітучі рослини для дому', 2],
      ['Екзотика', 'Рідкісні тропічні рослини для справжніх колекціонерів', 3],
    ];
    for (const [name, desc, order] of cats) {
      await client.query(
        `INSERT INTO categories (name, description, sort_order) VALUES ($1, $2, $3)
         ON CONFLICT (name) DO UPDATE SET description = EXCLUDED.description, sort_order = EXCLUDED.sort_order`,
        [name, desc, order]
      );
    }

    for (const p of PRODUCTS) {
      await client.query(
        'UPDATE products SET description = $1, characteristics = $2::jsonb WHERE id = $3',
        [p.description, JSON.stringify(p.characteristics), p.id]
      );
    }

    console.log('Categories table and product descriptions updated');
  } finally {
    await client.end();
  }
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
